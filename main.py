import tkinter as tk
from tkinter import ttk
from tkinter import * 
from Zakat import dataNama
Orang = dataNama("",0,"",0,0,0,"",0,0)
def clear():
    stringOutputKeterangan.set("")
def SubmitHarga():
    iNama = stringNama.get()
    iUmur = stringUmur.get()
    iPekerjaan = stringPekerjaan.get()
    iEmas = stringEmas.get()
    iPenghasilan = stringPenghasilan.get()
    Orang.setData(iNama,int(iUmur),iPekerjaan,int(iPenghasilan),iEmas)
    Orang.hitung()
    stringOutputNama.set(Orang.getnama())
    stringOutputUmur.set(Orang.getumur())
    stringOutputPekerjaan.set(Orang.getpekerjaan())
    stringOutputZakat.set(Orang.getzakat())
    stringOutputNizhab.set(Orang.getnisab())
    stringOutputKeterangan.set(Orang.getketerangan())

root = Tk()

root.geometry('450x530')
root.configure(background='#8FBC8F')
root.title('Program Perhitungan Zakat')
Button(root, text='Oke', bg='#2F4F4F', font=('arial', 12, 'normal'), command=SubmitHarga).place(x=164, y=240)
stringNama = StringVar()
tInputNama=Entry(root,textvariable=stringNama).place(x=210, y=22)
stringPekerjaan = StringVar()
