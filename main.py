stringEmas = IntVar()
tInputEmas=Entry(root,textvariable=stringEmas).place(x=274, y=185)
stringOutputNama = StringVar()
tOutputNama=Entry(root,state='readonly',textvariable=stringOutputNama).place(x=274, y=320)
stringOutputUmur = StringVar()
tOutputUmur=Entry(root,state='readonly',textvariable=stringOutputUmur).place(x=274, y=348)
stringOutputPekerjaan = StringVar()
tOutputPekerjaan=Entry(root,state='readonly',textvariable=stringOutputPekerjaan).place(x=274, y=376)
stringOutputNizhab = StringVar()
